package scoringsystem;

public class Stadium {
    int capacity;
    int stadiumId;
    String Name;

    public String getName() {
        return Name;
    }

    public int getCapacity() {
        return capacity;
    }


    public int getStadiumId() {
        return stadiumId;
    }

    public void setName(String name) {
        Name = name;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }


}
